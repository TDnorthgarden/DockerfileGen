func TestMergeRuns(t *testing.T) {
	instructions := []model.Instruction{
		{Type: model.InstRUN, Content: "apt-get update"},
		{Type: model.InstRUN, Content: "apt-get install -y curl"},
		{Type: model.InstRUN, Content: "rm -rf /var/lib/apt/lists/*"},
		{Type: model.InstWORKDIR, Content: "/app"},
	}

	merged := MergeRuns(instructions)
	if len(merged) != 2 {
		t.Fatalf("expected 2 instructions after merge, got %d", len(merged))
	}
	if merged[0].Type != model.InstRUN {
		t.Errorf("merged[0] type = %q, want RUN", merged[0].Type)
	}
	if !strings.Contains(merged[0].Content, "apt-get update") {
		t.Error("merged RUN missing first command")
	}
	if !strings.Contains(merged[0].Content, "apt-get install -y curl") {
		t.Error("merged RUN missing second command")
	}
	if merged[1].Type != model.InstWORKDIR {
		t.Errorf("merged[1] type = %q, want WORKDIR", merged[1].Type)
	}
}

func TestMergeRunsNoConsecutive(t *testing.T) {
	instructions := []model.Instruction{
		{Type: model.InstRUN, Content: "apt-get update"},
		{Type: model.InstWORKDIR, Content: "/app"},
		{Type: model.InstRUN, Content: "apt-get install -y curl"},
	}

	merged := MergeRuns(instructions)
	if len(merged) != 3 {
		t.Fatalf("expected 3 instructions (no merge), got %d", len(merged))
	}
}

