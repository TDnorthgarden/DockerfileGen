func TestRenderBasic(t *testing.T) {
	meta := model.ImageMetadata{
		BaseImageRef: "nginx:1.21-alpine@sha256:abc123",
		EnvVars:      map[string]string{"NGINX_VERSION": "1.21.0"},
		ExposedPorts: []string{"80/tcp"},
	}

	instructions := []model.Instruction{
		{Type: model.InstRUN, Content: "apk add --no-cache curl"},
		{Type: model.InstWORKDIR, Content: "/app"},
	}

	result := Render(meta, instructions, false)

	if !strings.Contains(result, "FROM nginx:1.21-alpine@sha256:abc123") {
		t.Error("missing FROM instruction")
	}
	if !strings.Contains(result, "RUN apk add --no-cache curl") {
		t.Error("missing RUN instruction")
	}
	if !strings.Contains(result, "WORKDIR /app") {
		t.Error("missing WORKDIR instruction")
	}
	if !strings.Contains(result, "# Generated by DockerfileGen") {
		t.Error("missing header comment")
	}
}

func TestRenderDeduplicatesMetadata(t *testing.T) {
	meta := model.ImageMetadata{
		BaseImageRef: "alpine:3.18",
		EnvVars:      map[string]string{"PATH": "/usr/bin"},
	}

	// History already has ENV — metadata ENV should not duplicate
	instructions := []model.Instruction{
		{Type: model.InstENV, KVPairs: map[string]string{"PATH": "/usr/bin"}},
	}

	result := Render(meta, instructions, false)

	count := strings.Count(result, "ENV ")
	if count != 1 {
		t.Errorf("ENV appears %d times, want 1 (dedup failed)", count)
	}
}

